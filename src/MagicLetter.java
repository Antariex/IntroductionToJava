/**
 Printing a magic letter

 Write a program that prints the following magic letter using * and whitespaces.

  Sample Output

 ********
       **
       **
       **
 **    **
 **    **
 *******
*/

public class MagicLetter {
    public static void main(String[] args) {
        System.out.println("********");
        System.out.println("      **");
        System.out.println("      **");
        System.out.println("      **");
        System.out.println("**    **");
        System.out.println("**    **");
        System.out.println(" *******");
    }
}
